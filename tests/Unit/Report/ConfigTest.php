declare(strict_types=1);

namespace Fisharebest\Webtrees\Tests\Unit\Report;

use Fisharebest\Webtrees\Contracts\TimestampInterface;
use Fisharebest\Webtrees\Tests\TestCase;
use PHPUnit\Framework\Attributes\CoversClass;
use Fisharebest\Webtrees\Report\PageOrientation;
use Fisharebest\Webtrees\Report\PaperSize;
use Fisharebest\Webtrees\Report\Config;

#[CoversClass(Config::class)]
class ConfigTest extends TestCase
{
    public function testConstructorAssignsProperties(): void
    {
        $timestamp = self::createStub(TimestampInterface::class);

        $config = new Config(
            paper_width:      612.0,
            paper_height:     792.0,
            left_margin:     36.0,
            right_margin:    36.0,
            top_margin:      36.0,
            bottom_margin:   36.0,
            header_margin:   9.0,
            footer_margin:   9.0,
            orientation:     PageOrientation::Portrait,
            paper_size:       PaperSize::USLetter,
            rtl:             false,
            generated_by:    'Generated by webtrees',
            author:          'Test Author',
            title:           'Test Report',
            description:     'A test report',
            align_rtl:       'right',
            entity_rtl:      'rlm',
            font:            'dejavusans',
            timestamp:       $timestamp,
        );

        self::assertSame(612.0, $config->paper_width);
        self::assertSame(792.0, $config->paper_height);
        self::assertSame(36.0, $config->left_margin);
        self::assertSame(36.0, $config->right_margin);
        self::assertSame(36.0, $config->top_margin);
        self::assertSame(36.0, $config->bottom_margin);
        self::assertSame(9.0, $config->header_margin);
        self::assertSame(9.0, $config->footer_margin);
        self::assertSame(PageOrientation::Portrait, $config->orientation);
        self::assertSame(PaperSize::USLetter, $config->paper_size);
        self::assertFalse($config->rtl);
        self::assertSame('Generated by webtrees', $config->generated_by);
        self::assertSame('Test Author', $config->author);
        self::assertSame('Test Report', $config->title);
        self::assertSame('A test report', $config->description);
        self::assertSame('right', $config->align_rtl);
        self::assertSame('rlm', $config->entity_rtl);
        self::assertSame('dejavusans', $config->font);
        self::assertSame($timestamp, $config->timestamp);
    }
}
